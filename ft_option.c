#include "ft_ls.h"

int	is_option(const char *s)
{
	return (s && s[0] == '-' && s[1] != '\0'
		&& !(s[1] == '-' && s[2] == '\0'));
}

int	is_end_of_options(const char *s)
{
	return (s && s[0] == '-' && s[1] == '-' && s[2] == '\0');
}

int	is_allowed_option(char c)
{
	return (c == 'l' || c == 'R' || c == 'a' || c == 'r' || c == 't');
}
